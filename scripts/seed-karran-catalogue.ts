async function main() {
  console.log(' Karran Sink Catalogue Seeding Script\n');

  // Get company ID from command line or find first company
  const companyIdArg = process.argv[2];

  let targetCompanyId: string;

  if (companyIdArg) {
    targetCompanyId = companyIdArg;
    console.log(`Using provided company ID: ${targetCompanyId}\n`);
  } else {
    console.log('No company ID provided. Looking up first company in database...');
    const [company] = await db.select().from(companies).limit(1);

    if (!company) {
      console.error(' No company found in database. Please pass a companyId as argument.');
      console.error('   Usage: tsx scripts/seed-karran-catalogue.ts <companyId>');
      process.exit(1);
    }

    targetCompanyId = company.id;
    console.log(` Using company: ${company.name} (${company.id})\n`);
  }

  let inserted = 0;
  let skipped = 0;

  console.log(`Processing ${KARRAN_SINKS.length} sink models...\n`);

  for (const sink of KARRAN_SINKS) {
    // Check if SKU already exists
    const existing = await db.query.sinks.findFirst({
      where: eq(sinks.sku, sink.sku),
    });

    if (existing) {
      console.log(`    SKIP: ${sink.sku} (already exists)`);
      skipped++;
      continue;
    }

    // Insert new sink
    await db.insert(sinks).values({
      companyId: targetCompanyId,
      ...sink,
    });

    console.log(`   INSERT: ${sink.sku} - ${sink.name}`);
    inserted++;
  }

  console.log('\n' + '='.repeat(70));
  console.log(` Seeding Complete!`);
  console.log(`   Inserted: ${inserted} sinks`);
  console.log(`   Skipped:  ${skipped} sinks (already existed)`);
  console.log(`   Total:    ${KARRAN_SINKS.length} models processed`);
  console.log('='.repeat(70) + '\n');

  process.exit(0);
}

main().catch((error) => {
  console.error(' Error seeding Karran catalogue:', error);
  process.exit(1);
});
